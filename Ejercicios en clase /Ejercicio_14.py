cadena = input('ingrese una palabra ')
c_a = 0
c_e = 0
c_i = 0
c_o = 0
c_u = 0
for char in cadena:
    if char=='a':
        c_a= c_a + 1
        print('cantida de a es ',c_a)
    if char=='e':
        c_e = c_e + 1
        print('cantidad de e es ',c_e)
    if char=='i':
        c_i = c_i + 1
        print('cantidad de e es ',c_i)
    if char=='o':
        c_o = c_o + 1
        print('cantidad de e es ',c_o)
    if char=='u':
        c_u = c_u + 1
        print('cantidad de e es ',c_u)